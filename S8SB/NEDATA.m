function[DM,AL,R1,R2,SN,CS,TH,P1,P2,HEN]=NEDATA(NE,TT,EYOUNG,PO,RR,ZZ,PRESS)
TH=TT(NE);
DM=zeros(6,6);
C=EYOUNG*TH/(1-PO*PO);
DM(1,1)=C;DM(2,2)=C;DM(1,2)=C*PO;DM(2,1)=C*PO;
DM(3,3)=C*0.5*(1-PO);
CB=C*TH*TH/12;
DM(4,4)=CB;DM(5,5)=CB;DM(4,5)=CB*PO;DM(5,4)=CB*PO;
DM(6,6)=CB*0.5*(1-PO);
%
R1=RR(NE);R2=RR(NE+1);TH=TT(NE);P1=PRESS(NE);P2=PRESS(NE+1);
Z12=ZZ(NE+1)-ZZ(NE);R12=R2-R1;
AL=sqrt(Z12*Z12+R12*R12);
SN=R12/AL;CS=Z12/AL;
HEN=zeros(8,8);
HEN(1,1)=CS;HEN(1,3)=SN;
HEN(5,5)=CS;HEN(5,7)=SN;
HEN(3,1)=-SN;HEN(3,3)=CS;
HEN(7,5)=-SN;HEN(7,7)=CS;
HEN(2,2)=1;HEN(4,4)=1;
HEN(6,6)=1;HEN(8,8)=1;
