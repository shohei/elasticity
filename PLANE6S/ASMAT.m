function [TK]=ASMAT(TK,SM,KAKOM,NE)
KI=(KAKOM(NE,1)-1)*2+(1:2);
KJ=(KAKOM(NE,2)-1)*2+(1:2);
KK=(KAKOM(NE,3)-1)*2+(1:2);
TK(KI,KI)=TK(KI,KI)+SM(1:2,1:2);
TK(KI,KJ)=TK(KI,KJ)+SM(1:2,3:4);
TK(KI,KK)=TK(KI,KK)+SM(1:2,5:6);
TK(KJ,KI)=TK(KJ,KI)+SM(3:4,1:2);
TK(KJ,KJ)=TK(KJ,KJ)+SM(3:4,3:4);
TK(KJ,KK)=TK(KJ,KK)+SM(3:4,5:6);
TK(KK,KI)=TK(KK,KI)+SM(5:6,1:2);
TK(KK,KJ)=TK(KK,KJ)+SM(5:6,3:4);
TK(KK,KK)=TK(KK,KK)+SM(5:6,5:6);
